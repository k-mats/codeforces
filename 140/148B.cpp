void solve() {
  double vp, vd, t, f, c;
  cin >> vp >> vd >> t >> f >> c;

  long cnt = 0;
  double xp = 0.0;
  double xd = 0.0;

  xp = vp * t;
  while (1) {
    //    DUMP3(xp, xd, cnt);
    double dt =  (xp - xd) / (vd - vp);
    xp += dt * vp;
    xd += dt * vd;
    //DUMP3(xp, xd, cnt);
    if (xp > c || fabs(xp - c) <= EPS) break;
    cnt++;
    xp += ((xd / vd) + f) * vp;
    xd = 0.0;
  }


  cout << cnt << endl;
}

int main(int argc, char *argv[]) {
  solve();

  return 0;
}
